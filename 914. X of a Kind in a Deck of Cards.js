var findGcd = (a, b) => {
	if (b === 0) {
		return a;
	}

	return findGcd(b, a % b);
};

var hasGroupsSizeX = function(deck) {
	if (deck.length <= 1) {
		return false;
	}

	let dict = {};
	let temp;
	let min = Infinity;

	for (let i = 0; i < deck.length; i++) {
		temp = deck[i];
		dict[temp] = dict[temp] || 0;
		dict[temp]++;
	}

	let keys = Object.keys(dict);

	for (let i = 1; i < keys.length; i++) {
		if (findGcd(dict[keys[i - 1]], dict[keys[i]]) < 2) {
			return false;
		}
	}

	return true;
};

